#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!lst)
		return ;
	if (!(last = ft_lstlast(*lst)))
	{
		*lst = new;
		return ;
	}
	last->next = new;
}
